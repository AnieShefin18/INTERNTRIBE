function add(a, b) {
  return a + b;
}
const multiply = function(a, b) {
  return a * b;
};
console.log("Addition:");
console.log("add =", add(8, 3));      
console.log("add =", add(2, 52));  

console.log("\nMultiplication:");
console.log("multiply =", multiply(9, 5));    
console.log("multiply =", multiply(6, 8));    
